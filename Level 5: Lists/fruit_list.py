# Create a list of 5 fruits and print them using a (While) loop

fruits = ['Apple', 'Mango', 'Banana', 'Orange']
i = 0
while i < len(fruits):
    print (fruits[i])
    i += 1
  
# Create a list of 5 fruits and print them using a (For)loop

fruits = ["Apple", "Banana", "Mango", "Orange", "Grapes"]

for fruit in fruits:
    print(fruit)
